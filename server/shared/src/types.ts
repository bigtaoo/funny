// Save data and contract types. SaveData is the single authoritative root of the meta system (META_DESIGN.md §3.1).
// Mirrors the client at client/src/game/meta/SaveData.ts (S0-1); this file is the authoritative server copy
// and does not depend on client/src/game. The SaveData schema in openapi.yml derives from this.

export type Rarity = 'common' | 'rare' | 'epic' | 'legendary';

export interface LevelRecord {
  timeMs?: number;
  leaked?: number;
  [k: string]: unknown;
}

// ── Equipment instances (EQUIPMENT_DESIGN §3.1) ─────────────────────────────
// Catalog (defId → slot/rarity/material/recipe) is in equipment.ts; combat stats are in @nw/engine.
import type { EquipRarity, EquipSlot } from './equipment';
import type { RankId } from './ladder';
import type { BattlePassData } from './battlepass';
import type { RetentionSave } from './retention';

/** Affix (unified shape for primary/secondary/special affixes); id points to the affix pool, value is the rolled number. */
export interface Affix {
  id: string;
  value: number;
}

/** Equipment instance (server-authoritative; enhancement level / affixes / rarity stored with the item). */
export interface EquipmentInstance {
  id: string; // instance id (generated by the server)
  defId: string; // equipment definition id (determines base stats/slot/material; see equipment.ts)
  rarity: EquipRarity; // denormalized cache of defId's rarity, for sorting/querying
  level: number; // enhancement level 0..9
  affixes: Affix[]; // affixes (can be rerolled via refinement)
  locked?: boolean; // prevents accidental use as enhancement fuel
}

/** Slot → instance id mapping for a single loadout. */
export type GearSlotMap = Partial<Record<EquipSlot, string /* instanceId */>>;

/** Gear loadout: phase one uses a global shared loadout for all units; byUnit is reserved for per-unit-type loadouts (EQUIPMENT_DESIGN §3.1/§3.2). */
export interface GearLoadout {
  global?: GearSlotMap;
  byUnit?: Record<string /* unitType */, GearSlotMap>;
}

/**
 * Character card instance in the Hero Roster (CHARACTER_CARDS_DESIGN §2.2).
 * Each instance is a unique owned card with independent level, XP, and equipment.
 * Multiple instances of the same card type can coexist in the roster (CC2).
 * SLG runtime fields (currentTroops, injuredUntil) are NOT stored here — they live in
 * worldsvc PlayerWorldDoc.cardState (§6.1).
 */
export interface CardInstance {
  /** Unique instance id generated by the server (e.g. 'card_a1b2c3'). Stable across the card's lifetime. */
  id: string;
  /** Card definition id — references CARD_DEFS[defId] in @nw/shared/cards (e.g. 'lichuang', 'max'). */
  defId: string;
  /** Card level 1–9. Increases via the feed system (CHARACTER_CARDS_DESIGN §3). */
  level: number;
  /** Accumulated XP within the current level (resets to 0 on level-up). */
  xp: number;
  /** Per-slot equipped item instance ids (weapon/armor/trinket → equipmentInstanceId). */
  gear: GearSlotMap;
  /** Locked cards cannot be used as feed material (CC4); prevents accidental consumption. */
  locked: boolean;
}

export interface SaveData {
  version: number;
  accountId: string;
  rev: number;
  updatedAt: number;

  // —— Server-authoritative section (client read-only, §2) ——
  // wallet/gacha: from S5 onward these are read-only mirrors of the commercial service's authority
  // (meta fills them in after receiving an economy operation receipt).
  wallet: { coins: number };
  inventory: {
    skins: string[];
    items: Record<string, number>;
  };
  gacha: { pity: Record<string, number> };
  // —— Monetization mirror (GACHA_DESIGN §5–§7). Read-only mirror of commercial authority (fate points / monthly card /
  //    starter packs), written by meta after economy operations + refreshed alongside GET /save. Lazily created; not in SyncPatch. ——
  monetization?: {
    fatePoints: number; // Fate Points balance (§7)
    subscriptionExpiry: number; // monthly card end timestamp (ms); 0 = none (§5)
    starterUsed: string[]; // one-off product ids already bought (§6)
  };
  // Fulfilled purchase orders (commercial orderId). Idempotent delivery ledger: redelivery uses $addToSet + $ne guard for deduplication (S5-5).
  deliveredOrders: string[];
  pvp: {
    elo: number;
    rank: string;
    wins: number;
    losses: number;
    streak: number;
    // —— S11 season fields (SEASON_DESIGN §4) ——
    /** Season number this pvp data belongs to; if behind the clock's current season, triggers lazy migration. */
    seasonNo: number;
    /** Highest ELO reached this season (tracked with max at each match settlement). */
    seasonPeakElo: number;
    /** Peak rank this season (derived from seasonPeakElo; TITLE_DESIGN season settlement reads this to award titles). */
    seasonPeakRank: RankId;
    /** Rank ids ever first-reached historically (lifetime ledger; idempotent guard for first-reach coin reward; not reset across seasons). */
    reachedRanks: RankId[];
  };

  // —— Stamina (A4). Server-authoritative, deducted in real time; natural regen 1 point/6 min, cap 120. Absent in old saves = treated as full. ——
  // regenAt = timestamp (ms) of the next +1 regen tick; 0 when already at cap (no timer needed).
  stamina?: { current: number; regenAt: number };

  // —— Titles (S10, TITLE_DESIGN §2). Server-authoritative; not writable via PUT /save. Absent = treated as empty set, lazily created. ——
  // Written by season settlement / achievement claim / admin grant; equipped['title'] is the equipped slot (client sync section).
  titles?: string[];

  // —— Battle pass (S11-C, SEASON_DESIGN §C). Server-authoritative; not writable via PUT /save. Absent = treated as not enrolled, lazily created. ——
  battlePass?: BattlePassData;

  // —— Retention system (B5, RETENTION_DESIGN). Server-authoritative; not writable via PUT /save. Lazily created. ——
  retention?: RetentionSave;

  // —— Achievement system (server-authoritative, ACHIEVEMENT_DESIGN §3). Lazily created to save storage; absent = treated as all-zero/empty. ——
  // Counters are written only at authoritative PvE/PvP settlement points (A2); the PUT /save allowlist
  // only accepts equipped/flags, so these three sections are structurally dropped (even if the client sends them they are never persisted) — no extra guard needed.
  stats?: Record<string, number>; // lifetime cumulative stats (StatKey → value), monotonically increasing
  achievements?: Record<string, { claimedTiers: number[] }>; // achId → subset of claimed tier numbers ⊆ [1,2,3]
  antiCheat?: {
    // PvP stat anti-cheat (§4.4), server-authoritative, client read-only or not sent at all
    statSuspicion: number; // accumulated cheat-hit count → determines inspection tier
    lastFlaggedTs?: number;
    // PvE anti-cheat (S4-4): count of replay recalculation mismatches; ban (pveBanned) on threshold.
    pveRejectCount?: number;
    pveBanned?: boolean;
  };

  // —— Client sync section (light validation, §2) ——
  progress: {
    cleared: string[];
    stars: Record<string, 1 | 2 | 3>;
    best: Record<string, LevelRecord>;
  };
  materials: Record<string, number>;
  /**
   * @deprecated Per-stat material upgrades from S3-2 (inf_hp/inf_dmg…). From CC-1 onward, unit progression
   * moves to the Hero Roster card instance system (cardInv). The engine no longer reads this field for
   * progression. Kept for old-save compatibility; will be retired after CC cleanup.
   */
  pveUpgrades: Record<string, number>;
  /**
   * @deprecated Collect-and-merge card inventory from S12 (`${unitId}:${level}` → count). Superseded by
   * the Hero Roster (cardInv) in CC-1. Retained for save compatibility; will be retired after CC cleanup.
   */
  cardInventory: Record<string, number>;
  /** Equipped cosmetics (slot → skinId). Purely visual, no combat effect, so still uploaded via the PUT /save sync section. */
  equipped: Record<string, string>;
  flags: Record<string, boolean>;

  // —— Equipment system (server-authoritative, EQUIPMENT_DESIGN §3.1). Not writable via PUT /save; only /equipment/* endpoints write. ——
  // Equipment instances are stored here; equipped-to-card mapping is now in CardInstance.gear (CC-1).
  // Absent in old saves → treated as empty; all read sites use `?? {}` as fallback (no migration runner, lazy defaults).
  equipmentInv: Record<string, EquipmentInstance>; // instanceId → instance

  // —— Hero Roster / card instance system (CC-1, CHARACTER_CARDS_DESIGN §2). Server-authoritative; not writable via PUT /save. ——
  // Hard cap: 150 cards. Equipment loadout now lives in CardInstance.gear (not a top-level gear field).
  // v3→v4 migration: unitLevels + gear are dropped (incompatible with new model); cardInv starts empty;
  // new-account onboarding gifts 3 starter cards (CHARACTER_CARDS_DESIGN §4).
  cardInv: Record<string, CardInstance>; // instanceId → CardInstance; max 150 entries
}

/**
 * Client sync fields accepted by PUT /save (SERVER_API.md §2.2).
 * From PVE_INTEGRITY_PLAN §8 onward, progress/materials/pveUpgrades are promoted to server-authoritative
 * (written only by /pve/* + ranked settlement), narrowing PUT /save to equipped/flags only.
 */
export type SyncPatch = Partial<Pick<SaveData, 'equipped' | 'flags'>>;

// v2 (2026-06-21): Added equipmentInv + gear (equipment system E0). Additive fields only; does not touch equipped.
// Old saves lazily default via `?? {}` at read sites; no destructive migration.
// v3 (2026-06-21): Unit progression rework (S12) — added unitLevels + cardInventory; pveUpgrades marked deprecated.
// Additive fields only; old saves lazily default to empty (game not yet live, no real saves; progression restarts from L1; no pveUpgrades→unitLevels conversion).
// v4 (2026-07-01): Hero Roster (CC-1, CHARACTER_CARDS_DESIGN §2). Removed unitLevels + gear (incompatible with card
// instance model). Added cardInv (hero roster, max 150 instances). cardInventory stays (deprecated, retire later).
// v3→v4 destructive migration: unitLevels + gear dropped unconditionally; cardInv starts empty.
// Players receive onboarding starter cards (§4) on first entry; old progression data is not converted.
export const SAVE_VERSION = 4;

/** Default save data for a new account. All authoritative sections start from zero. */
export function makeNewSave(accountId: string, now: number): SaveData {
  return {
    version: SAVE_VERSION,
    accountId,
    rev: 0,
    updatedAt: now,
    wallet: { coins: 0 },
    inventory: { skins: [], items: {} },
    gacha: { pity: {} },
    deliveredOrders: [],
    pvp: {
      elo: 1000,
      rank: 'unranked',
      wins: 0,
      losses: 0,
      streak: 0,
      seasonNo: 1,
      seasonPeakElo: 1000,
      seasonPeakRank: 'bronze' as RankId,
      reachedRanks: [],
    },
    progress: { cleared: [], stars: {}, best: {} },
    materials: {},
    pveUpgrades: {},
    cardInventory: {},
    equipped: {},
    flags: {},
    equipmentInv: {},
    cardInv: {},
  };
}
